const DEFAULT = PROFILES['yb-help'];

export function buildSiteCase(row = {}, site = {}) {
  const keyword = cleanText(row.mainKeyword || row.caseName || row.slug || '금융사기');
  const p = PROFILES[site.siteKey] || DEFAULT;
  const suppliedFaqs = [
    [cleanText(row.faq1Q), cleanText(row.faq1A)],
    [cleanText(row.faq2Q), cleanText(row.faq2A)],
    [cleanText(row.faq3Q), cleanText(row.faq3A)]
  ].filter(([q, a]) => q && a);

  const defaults = [
    [`${keyword} 피해금 회수가 가능한가요?`, '회수 가능성은 입금 시점, 자금 이동, 상대방 특정 가능성, 보유 자료에 따라 달라집니다. 입금내역과 대화자료를 기준으로 가능한 조치부터 확인해야 합니다.'],
    ['추가 입금을 하면 출금해 준다고 합니다.', '세금·보증금·인증비 등 추가 송금 요구는 위험 신호일 수 있습니다. 추가 입금 전에 현재 피해 정황과 자금 흐름을 먼저 점검해야 합니다.'],
    ['자료가 일부만 있어도 확인할 수 있나요?', '가능합니다. 입금내역, 계좌정보, 대화자료, 사이트 화면 중 남아 있는 자료부터 정리해 대응 우선순위를 검토할 수 있습니다.']
  ];

  return {
    ...row,
    caseName: keyword,
    mainKeyword: keyword,
    seoTitle: p.seoTitle(keyword),
    seoDescription: p.seoDescription(keyword),
    headline: p.title(keyword),
    subHeadline: p.lead(keyword),
    heroBadge: p.badge,
    eyebrow: p.eyebrow,
    noticeText: p.notice,
    sectionTitle: p.sectionA,
    processTitle: p.sectionB,
    risks: p.risks(keyword),
    triggers: p.triggers,
    factTitle: p.factTitle,
    factBody: p.factBody,
    processSteps: p.process,
    faqs: suppliedFaqs.length ? suppliedFaqs : defaults
  };
}
