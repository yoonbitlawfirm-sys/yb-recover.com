export function getCurrentSite(host = '') {
  const clean = String(host || '').toLowerCase().replace(/^www\./, '');
  const found = Object.values(SITE_PROFILES).find((site) =>
    site.domains.some((domain) => domain.replace(/^www\./, '') === clean)
  );
  return found || SITE_PROFILES['yb-recover.com'];
}

export function getSiteOrigin(site, url) {
  const host = url?.host || 'www.yb-recover.com';
  const protocol = url?.protocol || 'https:';
  return `${protocol}//${host}`;
}
